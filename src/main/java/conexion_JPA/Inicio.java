    private void CrearModelo() {
        try {
            modelo2 = (new DefaultTableModel(
                    null, new String[]{
                        "id", "Nombre",
                        "Descripción", "stock", "Precio", "Categoria"}) {
                Class[] types = new Class[]{
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class,
                    java.lang.String.class
                };
                boolean[] canEdit = new boolean[]{
                    false, false, false, false, false, false
                };

                @Override
                public Class getColumnClass(int columnIndex) {
                    return types[columnIndex];
                }

                @Override
                public boolean isCellEditable(int rowIndex, int colIndex) {
                    return canEdit[colIndex];
                }
            });
            tabla.setModel(modelo2);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString() + "error2");
        }
    }

    private void Cargar_Info() {
        try {
            Object o[] = null;
            List<Datos> listProducts = products.findDatosEntities();

            for (int i = 0; i < listProducts.size(); i++) {

                modelo2.addRow(o);
                modelo2.setValueAt(listProducts.get(i).getIdPostre(), i, 0);
                modelo2.setValueAt(listProducts.get(i).getNombre(), i, 1);
                modelo2.setValueAt(listProducts.get(i).getDescripcion(), i, 2);
                modelo2.setValueAt(listProducts.get(i).getStock(), i, 3);
                modelo2.setValueAt(listProducts.get(i).getPrecio(), i, 4);
                modelo2.setValueAt(listProducts.get(i).getCategoria(), i, 5);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());

        }
    }

