    private void button_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_guardarActionPerformed

        try {
            Datos p = new Datos();
            p.setNombre(text_postre.getText());
            p.setDescripcion(text_description.getText());
            p.setStock(Integer.parseInt(text_stock.getText()));
            p.setPrecio(Integer.parseInt(text_price.getText()));
            p.setCategoria(text_category.getText());

            JOptionPane.showMessageDialog(null, "Datos guardados con éxito");

            products.create(p);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_button_guardarActionPerformed

    private void button_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_eliminarActionPerformed

        try {
            int p = (int) tabla.getValueAt(tabla.getSelectedRow(), 0);

            products.destroy(p);
            JOptionPane.showMessageDialog(null, "Registro Eliminado Con Éxito");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_button_eliminarActionPerformed

